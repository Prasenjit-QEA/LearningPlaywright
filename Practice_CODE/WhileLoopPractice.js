// While Loop
// 1️⃣ Print "Playwright" 5 times
console.log("Printing Playwrite 5 times using ");
let i = 1;
while (i <= 5) {
    console.log(i + "--> Playwright");
    i++;
}

// 2️⃣ Print numbers from 1 to 10
console.log("Printing numbers from 1 to 10");
let j = 1;
while (j <= 10) {
    console.log(j);
    j++;
}

// 3️⃣ Print even numbers from 1 to 20
console.log("Printing even numbers from 1 to 10");
let k = 1;
while (k <= 10) {
    if (k % 2 === 0) {
        console.log(k);
    }
    k++;
}
// 4️⃣ Calculate sum of first 10 natural numbers
console.log("Calculating sum of first 10 natural numbers");
let l = 1;
let sum = 0;
while (l <= 10) {
    sum += l;
    l++;
}
console.log(sum);
// 5️⃣ Print the multiplication table of 7
console.log("Printing Multiplication Table of 7");
let m = 1;
while (m <= 10) {
    console.log("7 X " + m + " = " + 7 * m);
    m++;
}